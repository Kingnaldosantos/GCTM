//botao de abrir
var button_abrir1 = document.getElementById("button-ctm-net-abrir");
    button_abrir1.addEventListener("click",function(){
        var div_ctm_net =  document.getElementById("ctm-net");
        if(div_ctm_net.style.display    === "none"){
            div_ctm_net.style.display   =   "block";
        }
        else{
            div_ctm_net.style.display = "none";
        }
    })
